CREATE VIEW [dbo].[ATN_Absentism]
AS
SELECT     TOP (100) PERCENT dbo.ATN_Attendance.CardNo, dbo.HRM_Employees.EmployeeName, SUM(1 - dbo.ATN_Attendance.FinalValue) AS Days
FROM         dbo.ATN_Attendance INNER JOIN
                      dbo.HRM_Employees ON dbo.ATN_Attendance.CardNo = dbo.HRM_Employees.CardNo
WHERE     (MONTH(dbo.ATN_Attendance.AttenDate) = 5) AND (dbo.HRM_Employees.ActiveState = 1)
GROUP BY dbo.ATN_Attendance.CardNo, dbo.HRM_Employees.EmployeeName
HAVING      (SUM(1 - dbo.ATN_Attendance.FinalValue) > 0)
ORDER BY SUM(1 - dbo.ATN_Attendance.FinalValue)
GO
