CREATE VIEW [dbo].[ATNv_PunchTrendHR]
AS
SELECT     CardNo, AttenDate, REPLACE(STR(Punch1Time, 5, 2), '.', ':') AS t1, REPLACE(STR(Punch2Time, 5, 2), '.', ':') AS t2, CONVERT(datetime, 
                      '01/01/2012' + ' ' + REPLACE(STR(Punch1Time, 5, 2), '.', ':'), 103) AS dt1, CONVERT(datetime, '01/01/2012' + ' ' + REPLACE(STR(Punch2Time, 5, 2), '.', 
                      ':'), 103) AS dt2
FROM         dbo.ATN_Attendance
WHERE     (PunchStatusID = N'PR') AND (AttenDate >= CONVERT(datetime, '01/10/2012', 103)) AND (AttenDate <= CONVERT(datetime, '31/12/2012', 103)) AND 
                      (CardNo IN ('5014','5036','5029','5172','5181','5044','5171','5191','9472','9498'))
